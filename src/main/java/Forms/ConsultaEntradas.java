    public ConsultaEntradas() throws SQLException {
        initComponents();
        
        modelo = (DefaultTableModel) tablaDatos.getModel();
        String[] columnas = Atributos.Ingresos;
        
        for (int i = 0; i < 32; i++) {
            tablaDatos.getColumn(String.valueOf(i+1)).setHeaderValue(columnas[i]);
        }
        
        insertarDatos();
    }
    
    private void insertarDatos() throws SQLException{
        Busqueda buscar = new Busqueda();
        Object[] filas = buscar.obtenerTodosLosIngresos();
        
        for (Object fila: filas) modelo.addRow((Object[]) fila);
        
        tablaDatos.setModel(modelo);
    }

